export function normLang(v: string | null | undefined): Lang {
  const raw = String(v || "").trim();
  const lower = raw.toLowerCase().replace("_", "-");
  if (["en", "en-us", "en-gb", "english"].includes(lower)) return "en";
  if (["de", "de-de", "german", "deutsch"].includes(lower)) return "de";
  if (["pt-br", "pt", "pt-brasil", "portuguese", "português"].includes(lower)) return "pt-BR";
  return "pt-BR";
}

export function optionalLang(v: string | null | undefined): Lang | null {
  if (v === null || v === undefined || String(v).trim() === "") return null;
  return normLang(v);
}

export function tr(lang: string | null | undefined, key: string): string {
  const L = normLang(lang);
  return I18N[L][key] ?? I18N["pt-BR"][key] ?? key;
}

/** Translate + replace {placeholders}. */
export function trf(lang: string | null | undefined, key: string, vars: Record<string, string | number> = {}): string {
  let s = tr(lang, key);
  for (const [k, v] of Object.entries(vars)) {
    s = s.split(`{${k}}`).join(String(v));
  }
  return s;
}

/** Helper: fetch tenant.language with fallback. Pass an already-built supabase client. */
export async function getTenantLang(supabase: any, tenantId: string): Promise<Lang> {
  try {
    const { data } = await supabase
      .from("tenants")
      .select("language")
      .eq("id", tenantId)
      .maybeSingle();
    return optionalLang((data as any)?.language) || "pt-BR";
  } catch {
    return "pt-BR";
  }
}
